// 점수 계산 — 일진(일주)의 천간+지지 조합으로 결정론적 산출
function getFortuneScore(dayElement: string, stemIdx: number, branchIdx: number, category: string): number {
  const baseScores: Record<string, number[]> = {
    '목': [75, 80, 70, 85, 72, 78, 82, 68, 76, 73, 79, 71],
    '화': [80, 85, 72, 78, 82, 68, 76, 73, 75, 80, 70, 77],
    '토': [70, 75, 80, 82, 68, 76, 73, 75, 80, 85, 74, 69],
    '금': [82, 68, 76, 73, 75, 80, 85, 72, 78, 82, 67, 81],
    '수': [76, 73, 75, 80, 85, 72, 78, 82, 68, 76, 83, 74]
  };
  const scores = baseScores[dayElement] || baseScores['토'];
  const baseScore = scores[(stemIdx + branchIdx) % scores.length];
  const adjustments: Record<string, number> = {
    overall: 0,
    money: (branchIdx % 3 - 1) * 5,
    love: (stemIdx % 3 - 1) * 5,
    career: ((stemIdx + 1) % 3 - 1) * 5,
    health: ((branchIdx + 2) % 3 - 1) * 5
  };
  return Math.max(20, Math.min(100, baseScore + (adjustments[category] || 0)));
}

export function getDailyFortune(year: number, month: number, day: number): DailyFortuneData {
  const dateStr = `${year}-${month.toString().padStart(2, '0')}-${day.toString().padStart(2, '0')}`;
  const dayPillar = getDayPillar(year, month, day);
  const manseryokDay = getManseryokDay(year, month, day);
  const { yearGanzi, monthGanzi } = getMonthYearGanzi(year, month);

  const element = dayPillar.stemElement;
  const sIdx = dayPillar.stemIndex;   // 0-9
  const bIdx = dayPillar.branchIndex; // 0-11
  const ganziIdx = getGanziIdx(sIdx, bIdx); // 0-59

  const overallScore  = getFortuneScore(element, sIdx, bIdx, 'overall');
  const moneyScore    = getFortuneScore(element, sIdx, bIdx, 'money');
  const loveScore     = getFortuneScore(element, sIdx, bIdx, 'love');
  const careerScore   = getFortuneScore(element, sIdx, bIdx, 'career');
  const healthScore   = getFortuneScore(element, sIdx, bIdx, 'health');

  const luckyHours  = LUCKY_HOURS_BY_BRANCH[dayPillar.branch] || ['09:00-11:00', '15:00-17:00'];
  const directions  = ELEMENT_DIRECTIONS[element] || { lucky: '동쪽', avoid: '서쪽' };
  const luckyColors = ELEMENT_LUCKY_COLORS[element] || ['흰색', '금색'];

  const luckyNums = [
    ((sIdx + 1) % 9) + 1,
    ((bIdx + 2) % 9) + 1,
    ((sIdx + bIdx + 3) % 9) + 1
  ];

  const stem   = dayPillar.stem;
  const branch = dayPillar.branch;

  const goodPool  = GOOD_BY_STEM[stem]   ?? GOOD_BY_STEM['갑'];
  const avoidPool = AVOID_BY_BRANCH[branch] ?? AVOID_BY_BRANCH['자'];
  const goodThings  = pickThree(goodPool,  sIdx, bIdx);
  const avoidThings = pickThree(avoidPool, sIdx, bIdx);

  return {
    date: dateStr,
    lunarDate: `${manseryokDay.lunar} (음력)`,
    dayGanzi: manseryokDay.dayGanzi,
    dayHeavenlyStem: stem,
    dayEarthlyBranch: branch,
    dayElement: element,
    dayZodiac: dayPillar.zodiac,
    overallScore,
    overallFortune: OVERALL_60[ganziIdx],
    moneyScore,
    moneyFortune:  MONEY_BY_BRANCH[branch]  ?? MONEY_BY_BRANCH['자'],
    loveScore,
    loveFortune:   LOVE_BY_STEM[stem]       ?? LOVE_BY_STEM['갑'],
    careerScore,
    careerFortune: CAREER_BY_STEM[stem]     ?? CAREER_BY_STEM['갑'],
    healthScore,
    healthFortune: HEALTH_BY_BRANCH[branch] ?? HEALTH_BY_BRANCH['자'],
    luckyHours,
    luckyColors,
    luckyNumbers: [...new Set(luckyNums)],
    luckyDirection: directions.lucky,
    avoidDirection: directions.avoid,
    advice: ADVICE_60[ganziIdx],
    monthGanzi,
    yearGanzi,
    goodThings,
    avoidThings,
  };
}
