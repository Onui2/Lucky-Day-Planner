// 천간+지지를 조합해 풀에서 3개 선택 (결정론적)
// ─────────────────────────────────────────────
function pickThree(pool: string[], stemIdx: number, branchIdx: number): string[] {
  const n = pool.length;
  const i0 = (stemIdx * 3 + branchIdx) % n;
  const i1 = (stemIdx + branchIdx * 2 + 1) % n;
  const i2 = (stemIdx * 2 + branchIdx + 2) % n;
  const indices = [i0];
  if (i1 !== i0) indices.push(i1);
  if (i2 !== i0 && i2 !== i1) indices.push(i2);
  while (indices.length < 3) indices.push((indices[indices.length - 1] + 1) % n);
  return indices.slice(0, 3).map(i => pool[i]);
}

